        <h2>Historia de PHP</h2>
        <p>
            PHP (Hypertext Preprocessor) fue creado en 1994 por Rasmus Lerdorf como un conjunto de scripts en Perl para gestionar su currículum vitae. Más tarde, se expandió y se convirtió en un lenguaje de programación más robusto. En 1997, se lanzó PHP 3, que introdujo un motor de procesamiento más potente y la capacidad de interactuar con bases de datos. A lo largo de los años, PHP ha evolucionado significativamente, y versiones como PHP 5, lanzada en 2004, introdujeron programación orientada a objetos, mientras que PHP 7, lanzada en 2015, mejoró drásticamente el rendimiento y la eficiencia del lenguaje. Hoy en día, PHP es uno de los lenguajes más populares para el desarrollo web, alimentando a grandes plataformas como WordPress, Drupal y Joomla.
