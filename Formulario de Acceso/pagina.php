        <h2>Historia de GitHub</h2>
        <p>
            GitHub fue fundado en 2008 por Tom Preston-Werner, Chris Wanstrath, PJ Hyett y Scott Chacon como una plataforma para el control de versiones utilizando Git, un sistema de control de versiones creado por Linus Torvalds. La idea detrás de GitHub era facilitar la colaboración en proyectos de software y permitir a los desarrolladores compartir y contribuir a código abierto. En 2012, GitHub alcanzó los 1 millón de usuarios y se convirtió rápidamente en el lugar preferido para almacenar proyectos de código abierto. En 2018, Microsoft adquirió GitHub por 7.5 mil millones de dólares, lo que fortaleció aún más su posición en la comunidad de desarrollo. GitHub ha introducido varias características a lo largo de los años, como GitHub Actions, que permite la automatización de flujos de trabajo de desarrollo, y GitHub Copilot, una herramienta de inteligencia artificial para ayudar a escribir código.
