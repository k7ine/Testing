<?php

/*
Imprimir números del 1 al 10
Escribe un programa que use un bucle while para imprimir los números del 1 al 10 en pantalla.
*/

// Inicializamos la variable en 1
$i = 1;

// Usamos un bucle while para recorrer los números del 1 al 10
while ($i <= 10) {
    // Imprimimos el valor de $i seguido de un salto de línea HTML
    echo "$i<br>";

    // Incrementamos $i en 1 (equivalente a $i++)
    $i++;
}

?>
