<?php

/*
Muestra los números del 50 al 1.
Este programa utiliza un bucle while para imprimir los números desde el 50 hasta el 1 en orden descendente.
*/

// Inicializa la variable $i en 50
$i = 50;  

// Usamos un bucle while para mostrar los números del 50 al 1
while ($i >= 1) {  
    echo "$i <br>";  // Muestra el valor actual de $i
    $i--;  // Decrementa $i en 1 en cada iteración
}

?>
